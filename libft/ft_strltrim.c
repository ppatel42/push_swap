#include "libft.h"

size_t	ft_strltrim(char const *s)
{
	int		i;
	int		j;
	size_t	n;

	j = 0;
	n = 0;
	if (s)
	{
		while (s[j] == ' ' || s[j] == '\n' || s[j] == '\t')
			j++;
		i = j;
		while (s[i])
		{
			if (s[i] != ' ' && s[i] != '\n' && s[i] != '\t')
				n = i - j + 1;
			i++;
		}
	}
	return (n);
}
