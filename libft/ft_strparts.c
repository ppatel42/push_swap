int		ft_strparts(char const *s, char c)
{
	int	i;
	int	count;

	i = 0;
	count = 0;
	if (s && c)
	{
		while (s[i])
		{
			while (s[i] == c && s[i])
				i++;
			count++;
			while (s[i] != c && s[i])
				i++;
		}
	}
	return (count);
}
